<template>
  <div class="notes">

    <AddEditNote
      v-model="newNote"
      placeholder="Add a new note"
      ref="addEditNoteRef">
      <!-- #buttons is a shorthand to reach this slot: v-slot:buttons -->
      <template #buttons>
        <button
          @click="addNote"
          :disabled="!newNote"
          class="button is-link has-background-success">Add New Note</button>
      </template>
    </AddEditNote>

    <Note
      v-for="note in storeNotes.notes"
      :key="note.id"
      :note="note" />
  </div>
</template>

<script setup>
import { ref, watch } from 'vue'
import Note from '@/components/Notes/Note.vue'
import AddEditNote from '@/components/Notes/AddEditNote.vue'
import { useStoreNotes } from '@/stores/storeNotes'

/*
  store
*/

const storeNotes = useStoreNotes()


/*
  notes
*/

const newNote = ref('')
const addEditNoteRef = ref(null)

const addNote = () => {
  // getting date timestamp
  storeNotes.addNote(newNote.value)
  newNote.value = ''
  addEditNoteRef.value.focusTextarea()
}

/**
 * watch characters
 */

watch(newNote, (newValue) => {
  if (newValue.length === 100) {
    alert('Only 100 characters allowed gosh darnit!')
  }
})

</script>